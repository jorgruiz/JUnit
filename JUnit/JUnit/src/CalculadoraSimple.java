public class CalculadoraSimple {

    /**
     * Suma dos números.
     *
     * @param a Primer número.
     * @param b Segundo número.
     * @return La suma de a y b.
     */
    public int sumar(int a, int b) {
        return a + b;
    }

    /**
     * Resta dos números.
     *
     * @param a Primer número.
     * @param b Segundo número.
     * @return La diferencia entre a y b.
     */
    public int restar(int a, int b) {
        return a - b;
    }
}